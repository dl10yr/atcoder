n = int(input())
count = 0

if n < 1000:
    count = 0
elif n >= 10**3 and n < 10**6:
    count = 1 * (n-999)

elif n >= 10**6 and n < 10**9:
    count = 1 * (10**6 - 10**3) + 2 * (n - 10**6 + 1)

elif n >= 10**9 and n < 10**12:
    count = 1 * (10**6 - 10**3) + 2 * (10**9 - 10**6) + 3 * (n - 10**9 + 1)
    
elif n >= 10**12 and n < 10**15:
    count = 1 * (10**6 - 10**3) + 2 * (10**9 - 10**6) + 3 * (10**12 - 10**9) + 4 * (n - 10**12 + 1)

elif n >= 10**15 and n < 10**18:
    count = 1 * (10**6 - 10**3) + 2 * (10**9 - 10**6) + 3 * (10**12 - 10**9) + 4 * (10**15 - 10**12) + 5 * (n - 10**15 + 1)

print(count)